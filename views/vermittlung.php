    <p>Sie möchten Ihr Leben bereichern und einem tierischen Mitbewohner ein schönes Zuhause schenken?
        Unabhängig davon, ob Sie von einer verschmusten Samtpfote, einem putzigen Kaninchen oder einer
        schwanzwedelnden Spürnase träumen, mit der Aufnahme eines Tieres übernehmen Sie eine grosse und
        langjährige Verantwortung. Es ist deshalb wichtig, dass Sie vor Ihrem Entscheid die nachfolgenden
        Punkte kritisch überdenken.</p>

    <p>Haben Sie sich mit der Verhaltensweise und den konkreten Bedürfnissen Ihres Wunschtieres vertraut
        gemacht?</p>
    <p>Können Sie dem Platz- und Bewegungsbedarf des Tieres gerecht werden und haben Sie bedacht, dass
        gewisse
        Tierarten nur in Gruppen gehalten werden dürfen?</p>
    <p>Sind alle Familienmitglieder mit der Anschaffung des Haustieres einverstanden und bereit, sich, falls
        nötig, um seine Pflege zu kümmern?</p>
    <p>Sind Sie bereit, sich für lange Zeit an ein Haustier zu binden? Ein Hund kann je nach Grösse und
        Rasse zwischen 8 und 15 Jahre alt werden. Auch Katzen können teilweise ein stolzes Alter von bis zu
        20 Jahren, Kaninchen und Meerschweinchen bis zu 10 Jahre erreichen.</p>
    <p>Bietet Ihr Tagesablauf ausreichend Zeit, um die von Ihnen gewünschte Tierart optimal zu versorgen?
    </p>
    <p>Sind die Grundkosten für Versicherung, Steuer, Futter und Pflege durch das Haushaltsbudget abgedeckt?
    </p>
    <p>Ist noch ausreichend finanzieller Freiraum vorhanden, um für Standardtierarztkosten, wie auch
        unvorhergesehene Kosten durch Krankheit, Unfälle oder Operationen aufzukommen?</p>
    <p>Ist die Versorgung des Tieres sichergestellt, wenn Sie in den Urlaub fahren oder aus anderen Gründen
        nicht zu Hause sind?</p>
    <p>Erlaubt der Vermieter die Haustierhaltung?</p>
    <p>Könnte die Aufnahme des Haustieres zu Konflikten mit Nachbarn oder deren Haustieren führen?</p>
    <p>Leben schon andere Tiere in Ihrem Haushalt? Falls ja, haben Sie sich über die Verträglichkeit der
        Tiere Gedanken gemacht?</p>
    <p>Wenn Sie Wohnungskatzen aufnehmen möchten, muss der Balkon gesichert sein. Dürfen Sie auf Ihrem
        Balkon ein Katzennetz anbringen?</p>
    <p>Bei der Übernahme von Freilauf-Katzen ist eine Katzenklappe Bedingung. Dürfen und können Sie eine
        solche einbauen?</p>
    <p>Können Sie einen Freigänger während der Eingewöhnungszeit (mindestens 2 Monate) im Haus behalten und
        ihm danach den Freilauf in einem verkehrsarmen Quartier ermöglichen?</p>
    <p>Sind Sie empfindlich, was Schmutz und Schäden angeht? Katzen können an Möbeln und Tapeten kratzen,
        junge Hunde verrichten ihr Geschäft auch gerne einmal in der Wohnung und Tierhaare auf dem Teppich
        gehören zur Tagesordnung.</p>
    <p>Sind alle Familienmitglieder frei von Allergien?</p>
    <p>Fällt das Ergebnis aller notwendigen Überlegungen positiv aus, steht der Anschaffung eines Haustieres
        nichts mehr im Wege.</p>
