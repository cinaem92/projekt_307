    <h2>Willkommen bei Vermittlung vereint!</h2>
    <p>Wir sind ein gemeinnütziger Verein, welcher sich um die Vermittlung von heimatlosen Hunden in den
        Tierheimen und Tierschutzvereinen in der ganzen Schweiz kümmert.
    </p>
    <p>Haben Sie es sich gut überlegt? Verfügen Sie über die Zeit und die Mittel, sich die nächsten 10-15,
        vielleicht sogar 20 Jahre um einen Hund zu kümmern? Sind Sie sich bewusst, jeden Tag
        einen vierbeinigen Gefährten mit auf die Reise zu nehmen? Bei Krankheit zum Arzt zu gehen, keine
        Kosten zu scheuen, jeden Tag für Futter zu sorgen und bei Wind und Wetterung
        nach draussen spazieren zu gehen? Dann sind Sie bei uns richtig. Schauen Sie sich um. Vielleicht ist
        Ihr zukünftiger Wegbegleiter hier bei uns.
