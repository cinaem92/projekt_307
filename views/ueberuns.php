    <p>Wir sind ein gemeinnütziger Verein, welcher sich um die Vermittlung von heimatlosen Hunden in den
        Tierheimen und Tierschutzvereinen in der ganzen Schweiz kümmert.
        Die freiwilligen Helfer und Mitarbeiter finanzieren die Organisation selbst und mittels Spenden und
        sind auf die Bereitwilligkeit und Mithilfe sowie Bereitstellung der Daten durch die jeweiligen
        Drittpersonen angewiesen.
        <br> <br>
        Die Idee kam mir, weil ich selbst auf der Suche nach einem Hund war und die Suche nach <em>dem
            einen</em> Hund sich äusserst schwi rig gestaltete, da ich gut 50 Tierheim-Seiten durchforsten
        musste und immer noch nicht fündig wurde - und schliesslich aufgab. Vielen Menschen geht es genauso,
        und die Leidtragenden sind schlussendlich die Tiere, weil sie kein Zuhause finden. Ich habe mir also
        überlegt, eine Seite zu kreieren, auf der der User die Möglichkeit erhält, mittels Suchfunktion
        gleich alle Tierheime gleichzeitig zu durchforsten.
